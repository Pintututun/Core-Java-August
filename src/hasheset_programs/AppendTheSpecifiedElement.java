package hasheset_programs;

import java.util.HashSet;

public class AppendTheSpecifiedElement {
public static void main(String[] args) {
	HashSet<String> list=new HashSet<String>();
	list.add("Red");
	list.add("Green");
	list.add("Black");
	list.add("White");
	list.add("Pink");
	list.add("Yellow");
	System.out.println("The hash set:"+list);
}
}
